# Results

This analysis explored spatial and temporal trends in the relative abundances of dozens of microbial groups at 5 sites within the United States (Fig 1). Overall, spatial variability eclipsed temporal variability (Fig 2), though many groups displayed sensitivity to environmental conditions (Fig 3). Our predictive ability varied by taxonomic ranking (Fig 4). The majority of our predictive uncertainty came from process error (Fig 5), indicating either that more complexity to capture microbial abundance trends, or that there is inherent stochasticity that statistical models cannot explain.

